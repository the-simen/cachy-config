echo "⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿"
echo "⠀⠀⠀⠀⠀      ========= Web App Creator⠀=========⠀⠀⠀          "

read -rp "App name: " NAME
read -rp "URL: " URL
read -rp "Icon (name OR URL): " ICON_INPUT
echo "Browser (chromium - default / brave / zen / firefox)"
read -rp "skip to use default: " BROWSER

# sanitize name
FILE_NAME=$(echo "$NAME" | tr ' ' '-' | tr '[:upper:]' '[:lower:]')

# --- validate URL ---
if ! curl -s --head --fail "$URL" > /dev/null; then
  echo "Error: URL is not reachable"
  read -n 1 -s -r -p "Press any key to close..."
  echo
  kill -TERM "$PPID"
fi

# --- icon handling ---
if [[ "$ICON_INPUT" =~ ^https?:// ]]; then
  ICON_PATH="$ICON_DIR/${FILE_NAME}.png"

  echo "Checking icon URL..."

  # check headers
  CONTENT_TYPE=$(curl -sI "$ICON_INPUT" | grep -i content-type | awk '{print $2}' | tr -d '\r')

  if [[ "$CONTENT_TYPE" != image/* ]]; then
    echo "Warning: URL does not point to an image (Content-Type: $CONTENT_TYPE)"
    echo "Falling back to default icon"
    ICON="web-browser"
  else
    echo "Downloading icon..."
    if curl -L --fail "$ICON_INPUT" -o "$ICON_PATH"; then
      ICON="$ICON_PATH"
    else
      echo "Failed to download icon, using fallback"
      ICON="web-browser"
    fi
  fi
else
  ICON="$ICON_INPUT"
fi

# --- browser exec ---
case "$BROWSER" in
  "" | chromium)
    EXEC="chromium --app=$URL"
    ;;
  brave)
    EXEC="brave-browser --app=$URL"
    ;;
  zen)
    EXEC="zen --new-window $URL"
    ;;
  firefox)
    EXEC="firefox --new-window $URL"
    ;;
  *)
    echo "Unknown browser, fallback to chromium"
    EXEC="chromium --app=$URL"
    ;;
esac

DESKTOP_FILE="$APP_DIR/${FILE_NAME}.desktop"

cat > "$DESKTOP_FILE" <<EOF
[Desktop Entry]
Name=$NAME
Exec=$EXEC
Icon=$ICON
Type=Application
Categories=Network;
StartupNotify=true
EOF

echo ""
echo "Created: $DESKTOP_FILE"
echo "Done."
